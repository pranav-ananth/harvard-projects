#include <iostream>
using namespace std;

int main() {
	int num1 = 5; 
	int num2 = 4;
	cout << "Number 1 is " << num1 << endl;
	cout << "Number 2 is " << num2 << endl;
	
	int sum = num1 + num2;
	int product = num1 * num2;
	cout << "The sum of the two numbers is " << sum << endl;
	cout << "The product of the two numbers is " << product << endl;
}
